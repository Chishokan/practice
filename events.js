// 大会エピソードの既読キー
function cupStoryKey(key, kind, round) {
  return round == null ? `${key}:${kind}` : `${key}:${kind}:${round}`;
}

// その回戦の直前に出す話（無ければ null）
function getCupBefore(key, round) {
  const ep = CUP_STORY[key]?.before?.[round];
  if (!ep) return null;
  if (state.cupSeen?.includes(cupStoryKey(key, "before", round))) return null;
  return ep;
}

// 決勝を終えた直後に出す話（無ければ null）
function getCupAfter(key) {
  const list = CUP_STORY[key]?.after;
  if (!list) return null;
  if (state.cupSeen?.includes(cupStoryKey(key, "after"))) return null;
  return list.find(v => v.when());
}

// 話を確定させ、既読にして、表示用のデータを返す。
// メインストーリーと同じ形（title/pages/cast/image）にして、同じビューアで見せる。
function takeCupStory(ep, key, kind, round) {
  const pages = ep.pages();
  const cast = (typeof ep.cast === "function" ? ep.cast() : ep.cast) ?? [];
  const data = { title: ep.title, pages, cast: cast.filter(Boolean), image: ep.image ?? null };

  if (!state.cupSeen) state.cupSeen = [];
  state.cupSeen.push(cupStoryKey(key, kind, round));
  // 履歴から読み返せるように、出た版をそのまま残す
  if (!state.cupRead) state.cupRead = {};
  state.cupRead[cupStoryKey(key, kind, round)] = data;

  return data;
}


// その週のストーリー（なければ null）
function getStory(week) {
  return STORY.find(s => s.week === week) || null;
}

// 条件に合う最初の分岐を選ぶ
function pickVariant(story) {
  return story.variants.find(v => v.when()) || null;
}


