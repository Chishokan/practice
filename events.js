// ランダムにn人選ぶ（数名イベント用）
function randomFew(n, pool) {
  const list = [...(pool ?? activePlayers())].sort(() => Math.random() - 0.5);
  return list.slice(0, n);
}

// 「赤星・青木」のように名前を並べる
function names(list) {
  return list.map(p => p.name).join("・");
}

