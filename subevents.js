//   ・1週に出るのは1つまで。まとめて来ると読み飛ばされる。
//   ・同じ週にメインストーリーとぶつかったら、メインを読んでから出す。
//
// 確率とレア度の設計:
//
//   ステップ1の出やすさは、話ごとに変える。全部を同じ確率にすると、
//   「どれが起きても同じ」になり、引きの面白さが出ない。
//
//   ★1（よく起きる）   … 条件なし。夏までにはだいたい始まる
//   ★2（条件しだい）   … 条件を満たすと大きく跳ねる。満たさなくても細く出る
//   ★3（レア）        … 条件を満たしても、そう簡単には出ない
//
//   条件を満たしていなくても出るようにしてあるのは、
//   「試合を1回もしていないと永久に見られない話」を作らないため。
//   ただし細い（base）。条件を満たすと bonus に跳ね上がる。
//
//   ステップ2以降は、始まった話が途中で止まると気持ち悪いので高めにする。
//   実測で 6月2週までに約1本、12月までに2〜3本が完走するよう合わせた。
// ステップ2・3の週あたり確率。ステップ1はレア度で決まるので使わない。
// 始まった話が途中で止まると気持ち悪いので、後の段ほど高くしてある。
const SUB_RATE = [0, 0.20, 0.25];

// 段と段の間は最低これだけあける。
// 続きが翌週に来ると、関係が進んだ感じがしない。
const SUB_COOLDOWN = 2;

// レア度ごとの、ステップ1の週あたり確率。
//   base  … 条件を満たしていないとき
//   bonus … 条件を満たしたとき
//
// 数字は実測で決めた。1シーズン600回ずつ回して、
//   6月2週までに 1.29本 / 12月までに 2.49本が完走
// になるところを選んである。
